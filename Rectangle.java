class RectanglePattern {
    public static void main (String args[]) {
        int length = 5 ; 
        int breadth = 7 ; 
        
         for(int i = 0 ; i < length ; i++){
            for(int j = 0 ; j < breadth ; j++){
                System.out.print("*") ; 
            }
            System.out.println(" ") ; 

    }
}
}